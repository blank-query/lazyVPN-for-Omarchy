func TestDetectProviderByDNS(t *testing.T) {
	tests := []struct {
		name string
		dns  string
		want string
	}{
		{"protonvpn", "10.2.0.1", "protonvpn"},
		{"protonvpn comma", "10.2.0.1, fd00::1", "protonvpn"},
		{"mullvad primary", "10.64.0.1", "mullvad"},
		{"mullvad secondary", "193.138.218.74", "mullvad"},
		{"ivpn", "172.16.0.1", "ivpn"},
		{"airvpn", "10.128.0.1", "airvpn"},
		{"nordvpn", "103.86.96.100", "nordvpn"},
		{"surfshark", "162.252.172.57", "surfshark"},
		{"windscribe", "10.255.255.1", "windscribe"},
		{"fastestvpn", "10.8.0.1", "fastestvpn"},
		{"unknown dns", "8.8.8.8", ""},
		{"empty dns", "", ""},
		{"comma prefix mullvad", "fd00::1,10.64.0.1", "mullvad"},
		{"space prefix mullvad", "fd00::1 10.64.0.1", "mullvad"},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			cfg := &wireguard.Config{DNS: tt.dns}
			got := detectProvider("", cfg)
			if got != tt.want {
				t.Errorf("detectProvider(dns=%q) = %q, want %q", tt.dns, got, tt.want)
			}
		})
	}
}

