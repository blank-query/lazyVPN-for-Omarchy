func TestDetectProviderDNSTakesPriority(t *testing.T) {
	// DNS detection should match before endpoint detection
	cfg := &wireguard.Config{
		DNS:      "10.2.0.1",
		Endpoint: "se-got-wg-001.mullvad.net:51820",
	}
	got := detectProvider("", cfg)
	if got != "protonvpn" {
		t.Errorf("DNS should take priority over endpoint: got %q, want protonvpn", got)
	}
}

func TestDetectProviderCaseInsensitive(t *testing.T) {
	// DNS is lowercased, so mixed case should work
	cfg := &wireguard.Config{DNS: "10.2.0.1"}
	got := detectProvider("", cfg)
	if got != "protonvpn" {
		t.Errorf("got %q, want protonvpn", got)
	}

	// Endpoint with mixed case
	cfg2 := &wireguard.Config{Endpoint: "node-US-01.PROTONVPN.NET:51820"}
	got2 := detectProvider("", cfg2)
	if got2 != "protonvpn" {
		t.Errorf("mixed-case endpoint: got %q, want protonvpn", got2)
	}
}

